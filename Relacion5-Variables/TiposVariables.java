public class TiposVariables {
    public static void main(String[] args) {
        /* a) variable entera */
        int p = 10; 
        int q = 20;
        
        /* b) variable float */
        float x = 2.3f;
        float y = 4.4f;
        float z = 6.6f;

        /* c) variable de caracter */
        char a = 'a';
        char b = 'b';
        char c = 'c';

        /* d) variable double */
        double raiz1 = 1.2356;
        double raiz2 = 2.3478;

        /* e) variable entera larga */
        long contador = 1234567;

        /* f) variable entera larga */
        short indicador = 32000;

        /* g) variable entera indice */
        int indice = 3;

        /* h) variables double*/
        double precio = 3.99;
        double preciofinal = 4.99;

        /* variables caracter */
        char car1 = 'x';
        char car2 = 'y';
        
        /* variable byte */
        byte valor = 126;

        /* variables logicas */
        boolean primero = true;
        boolean ultimo = false;

        /* variable tipo cadena de caracteres */
        String nombre = "Javi";
    }
}
